#!/bin/bash
#
# run.sh
#



#-----------------------------------------------------------------------------------------------------------------------
# load environment variables
#-----------------------------------------------------------------------------------------------------------------------
source "${DIGCOLPROC_HOME}setup.sh" $0 "$@"



#-----------------------------------------------------------------------------------------------------------------------
# Is the dataset validated?
#-----------------------------------------------------------------------------------------------------------------------
echo "Start preparing ingest...">>$log
cf=$fileSet/.validate/concordanceValidWithPID.csv
if [ ! -f $cf ] ; then
    echo "Error... did not find $cf">>$log
    echo "Is the dataset validated ?">>$log
    exit -1
fi

md5check=$(md5sum $fileSet/$archiveID.csv)
md5=$(cat $fileSet/.validate/$archiveID.csv.md5)
if [ "$md5" != "$md5check" ] ; then
    echo "The CSV file seems to have been changed after it was validated and must be re-validated first.">>$log
    exit -1
fi



#-----------------------------------------------------------------------------------------------------------------------
# TODO: Transform to flow 4 directory layout?
#-----------------------------------------------------------------------------------------------------------------------



#-----------------------------------------------------------------------------------------------------------------------
# Determine access value, default is 'open'
#-----------------------------------------------------------------------------------------------------------------------
access_file=$fileSet/.access.txt
if [ -f "$access_file" ] ; then
	access=$(<"$access_file")
fi
if [ -z "$access" ] ; then
  access="open"
  echo "$access" > ${fileSet}/.access.txt
fi



#-----------------------------------------------------------------------------------------------------------------------
# Start the ingest
#-----------------------------------------------------------------------------------------------------------------------
source ../ingest.sh



#-----------------------------------------------------------------------------------------------------------------------
# TODO: PID binding? EAD validation again?
#-----------------------------------------------------------------------------------------------------------------------



#-----------------------------------------------------------------------------------------------------------------------
# End job
#-----------------------------------------------------------------------------------------------------------------------
echo "Done. All went well at this side." >> $log
exit 0